package Examen160523;

public class AntivirusException extends Exception {

	public AntivirusException() {
		super();
	}

	public AntivirusException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public AntivirusException(String message, Throwable cause) {
		super(message, cause);
	}

	public AntivirusException(String message) {
		super(message);
	}

	public AntivirusException(Throwable cause) {
		super(cause);
	}

}
